#No caso vamos utilizar um programa para ver quanto uma pessoa está devendo.

print('\nExemplo de sequência e tempo.')
divida = 0
compra = 100
divida = divida + compra
compra = 200
divida = divida + compra
compra = 300
divida = divida + compra
compra = 0
print(divida)

