#Exercício 5.1 - Programa que mostre de 1 a 100.

print('Programa para mostrar de 1 a 100 utilizando o comando while:')
x = 1
while x <= 100:
    print(x)
    x = x + 1

#Exercício 5.2 - Programa que mostre de 50 a 100.

print('\nPrograma para mostrar de 50 a 100 utilizando o comando while:')
x = 50
while x <= 100:
    print(x)
    x = x + 1

#Exercício 5.3 - Programa de contagem regressiva de "10 - 0 e FOGO!".

print('Contagem regressiva para o lançamento:')
contagem = 10
while contagem >= 0:
    print(contagem)
    contagem = contagem - 1
print('FOGO!')

